class ZFEngine_View_Helper_FormValue extends Zend_View_Helper_FormHidden
{
    /**
     * Generates a 'hidden' element.
     *
     * @access public
     *
     * @param string|array $name If a string, the element name.  If an
     * array, all other parameters are ignored, and the array elements
     * are extracted in place of added parameters.
     * @param mixed $value The element value.
     * @param array $attribs Attributes for the element tag.
     * @return string The element XHTML.
     */
    public function formValue($name, $value = null, array $attribs = null)
    {
        $hidden = parent::formHidden($name, $value, $attribs);
        $hash = parent::formHidden($name . '_check', ZFEngine_Form_Element_Value::getHash($name, $value));

        return $hidden . $hash;
    }
}
